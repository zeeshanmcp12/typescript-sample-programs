
function cityCountry(city, country) {
    // console.log(`${city} is in ${country}.`);
    return `"${city}, ${country}"`
}

const resultOne = cityCountry('Karachi','Pakistan');
console.log(resultOne)

const resultTwo = cityCountry('Doha','Qatar');
console.log(resultTwo)

